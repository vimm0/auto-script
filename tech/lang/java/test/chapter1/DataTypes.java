// Variables and Datatypes
public class DataTypes {
    public static void main(String[] args) {
        byte b = 10;
        short s = 20;
        int i = 30;
        long l = 401;
        float f = 50f;
        char c = 'A';
        double d = 60d;
        boolean bln = true;
        String str = "Hello";
        System.out.println(b + " " + s + " " + i + " " + f + " " + l + " " + c + " " + d + " " + bln + " " + str);
    }
}